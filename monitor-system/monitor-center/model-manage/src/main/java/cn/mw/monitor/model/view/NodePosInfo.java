package cn.mw.monitor.model.view;

public class NodePosInfo {
    private int pos;
    private int comboPos;

    private int level;

    public int getPos() {
        return pos;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }

    public int getComboPos() {
        return comboPos;
    }

    public void setComboPos(int comboPos) {
        this.comboPos = comboPos;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }
}
